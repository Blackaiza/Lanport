    <script>
        function togglePassword(fieldId) {
            const passwordField = document.getElementById(fieldId);
            const button = passwordField.nextElementSibling;
            const icon = button.querySelector('svg');

            if (passwordField.type === 'password') {
                passwordField.type = 'text';
                icon.innerHTML = '<path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.88 9.88l-3.29-3.29m7.532 7.532l3.29 3.29M3 3l3.59 3.59m0 0A9.953 9.953 0 0112 5c4.478 0 8.268 2.943 9.543 7a10.025 10.025 0 01-4.132 5.411m0 0L21 21" />';
            } else {
                passwordField.type = 'password';
                icon.innerHTML = '<path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" /><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z" />';
            }
        }

        const passwordInput = document.getElementById('password');
        const submitButton = document.getElementById('submitButton');
        let allRulesMet = false;

        passwordInput.addEventListener('input', function() {
            const password = this.value;

            // Check length (8-50 characters)
            const lengthValid = password.length >= 8 && password.length <= 50;
            updateRuleStatus('length', lengthValid);

            // Check uppercase
            const hasUppercase = /[A-Z]/.test(password);
            updateRuleStatus('uppercase', hasUppercase);

            // Check lowercase
            const hasLowercase = /[a-z]/.test(password);
            updateRuleStatus('lowercase', hasLowercase);

            // Check number
            const hasNumber = /[0-9]/.test(password);
            updateRuleStatus('number', hasNumber);

            // Check symbol
            const hasSymbol = /[!@#$%^&*(),.?":{}|<>]/.test(password);
            updateRuleStatus('symbol', hasSymbol);

            // Check Latin letters only
            const isLatin = /^[a-zA-Z0-9!@#$%^&*(),.?":{}|<>]+$/.test(password);
            updateRuleStatus('latin', isLatin);

            // Check if all rules are met
            allRulesMet = lengthValid && hasUppercase && hasLowercase && hasNumber && hasSymbol && isLatin;
            submitButton.disabled = !allRulesMet;
        });

        function updateRuleStatus(ruleId, isValid) {
            const ruleElement = document.getElementById(ruleId);
            const span = ruleElement.querySelector('span');
            if (isValid) {
                span.classList.remove('text-red-500');
                span.classList.add('text-green-500');
            } else {
                span.classList.remove('text-green-500');
                span.classList.add('text-red-500');
            }
        }
    </script>
